void IsiRak(char Rak[], int jml)
{
    for (size_t i = 0; i < jml; i++)
    {
        Rak[i] = 'A' + i;
    }
}

void MakeNULL(char Rak[])
{
    for (size_t i = 0; i < sizeof(Rak); i++)
    {
        Rak[i] = '\0';
    }
}

